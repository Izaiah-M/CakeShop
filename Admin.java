package cakeShop;

public class Admin {
	private String name;
	private String email;
	private String contact;
	private String address;
	
	public Admin() {
		
	}
	
	public String getName() {
		return name;
	}
	
	public String getEmail() {
		return email;
	}
	
	public String getContact() {
		return contact;
	}
	
	public String getAddress() {
		return address;
	}
	
}
